      public void LoadForm(ArrayList<SanPham> dsSanPham){
//        SanPhamDAO product = new SanPhamDAO();
//        ArrayList<SanPham> dsSanPham = new ArrayList<>();
//        dsSanPham = SanPhamDAO.layDanhsachSanPham();
        DefaultTableModel defaultTableModel = (DefaultTableModel) this.jtblProduct.getModel();
        defaultTableModel.setColumnCount(0);
        defaultTableModel.setRowCount(0);
        defaultTableModel.addColumn("Mã sản phẩm ");
        defaultTableModel.addColumn("Tên sản phẩm ");
        defaultTableModel.addColumn("Giá");
        defaultTableModel.addColumn("Hạn sử dụng");
        defaultTableModel.addColumn("Số Lượng");
        defaultTableModel.addColumn("Mã Loại");
        defaultTableModel.addColumn("Mã nhà cung cấp");
     
        for(SanPham nv : dsSanPham){
            Vector vec = new Vector();
              vec.add(nv.getIdproduct());
            vec.add(nv.getName());
            vec.add(nv.getPrice());
            vec.add(nv.getDateproduct());
            vec.add(nv.getQuantity());
            vec.add(nv.getIdcategory());
            vec.add(nv.getIdsupplier());
            defaultTableModel.addRow(vec);
        }
        TableColumnModel columnModel = jtblProduct.getColumnModel();
        columnModel.getColumn(1).setPreferredWidth(150); // Độ rộng cho cột "Name"
        columnModel.getColumn(2).setPreferredWidth(150);  // Độ rộng cho cột "Age"
        columnModel.getColumn(4).setPreferredWidth(100); 
        JTableHeader header = jtblProduct.getTableHeader();
        header.setFont(new Font("Arial", Font.BOLD, 16)); // Font Arial, đậm, kích cỡ 14
    }

