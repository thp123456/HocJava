    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        // TODO add your handling code here:
           if(txtMaSP.getText().isEmpty()){
            JOptionPane.showMessageDialog(this, "Chưa chọn sản phẩm cần xóa!");
        }
        else{
            String idproduct = txtMaSP.getText();
            String name = txtTen.getText();
              String price= txtGia.getText();
            String dateproduct= txtHSD.getText();
            String quantity= txtSoLuong.getText();
            String idcategory= txtMaLoai.getText();
            String idsupplier= txtMaNCC.getText();
            
            SanPhamDAO.updateProduct(idproduct, name, price, dateproduct, quantity, idcategory, idsupplier);
            LoadForm(SanPhamDAO.layDanhsachSanPham());
            JOptionPane.showMessageDialog(this, "Cập nhật thành công!");
        
        }
    }//GEN-LAST:event_jButton3ActionPerformed

