    private void btnLoginActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnLoginActionPerformed
        // TODO add your handling code here:
        String user,pass;
        user = txtUsername.getText();
        pass = txtPassWord.getText();
        if (user.isEmpty() || pass.isEmpty()) {
            JOptionPane.showMessageDialog(rootPane, "Vui lòng nhập đầy đủ thông tin đăng nhập!");
        }
        else
        {
            SQLServerProvider provider = new SQLServerProvider();
            if (provider.checkConnection(user, pass)) {
                DangNhapDAO dn = new DangNhapDAO();
                String id = dn.getIdEmployeesByUsername(user);
                if (!id.isEmpty()) {
                    NhanVienDAO nv = new NhanVienDAO();
                    NhanVien emp = emp = nv.getInformationEmployeebyID(id);
                    if (emp != null) {
                        Main f = new Main(emp);
                        f.setVisible(true);
                        setVisible(false);
                    }
                }
           
        }   
             else{
                JOptionPane.showMessageDialog(this, "Đăng Nhập thất bại!");
            }
        }
    }//GEN-LAST:event_btnLoginActionPerformed

